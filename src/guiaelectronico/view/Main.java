    private void btnCrearNotaCreditoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearNotaCreditoActionPerformed
        // TODO add your handling code here:
        CabeceraJsonGuia objCabJsonGuia = new CabeceraJsonGuia();
        objCabJsonGuia.setFecEmision(this.clsUtil.formatoFechaYYYYMMDD(txtFechaEmision.getText()));
        objCabJsonGuia.setHorEmision(this.clsUtil.capturarHora());
        objCabJsonGuia.setTipDocGuia("09");
        cbxUniMed.getItemAt(cbxUniMed.getSelectedIndex()).getCodSunat();
        objCabJsonGuia.setSerNumDocGuia( cbxSerieCorreGuia.getItemAt( cbxSerieCorreGuia.getSelectedIndex() ).getSerie()+"-"+this.clsUtil.formatoCorrelativo(8, cbxSerieCorreGuia.getItemAt( cbxSerieCorreGuia.getSelectedIndex() ).getCorrelativo()) );
        objCabJsonGuia.setNumDocDestinatario(txtRuc.getText());
        objCabJsonGuia.setTipDocDestinatario("6");
        objCabJsonGuia.setRznSocialDestinatario(txtDirecDesti.getText()+"|"+txtDirecDesti2.getText());
        objCabJsonGuia.setMotTrasladoDatosEnvio("01");
        objCabJsonGuia.setDesMotivoTrasladoDatosEnvio("VENTA");
        objCabJsonGuia.setIndTransbordoProgDatosEnvio(false);
        Double pesoTotal = Double.parseDouble(txtPesoBrutoTotal.getText());
        objCabJsonGuia.setPsoBrutoTotalBienesDatosEnvio(pesoTotal);
        objCabJsonGuia.setUniMedidaPesoBrutoDatosEnvio( cbxPesoBrutoTotal.getItemAt( cbxPesoBrutoTotal.getSelectedIndex()).getCodSunat() );
        objCabJsonGuia.setNumBultosDatosEnvio(Double.parseDouble(txtNumeroTotalBultos.getText()));
        objCabJsonGuia.setModTrasladoDatosEnvio("01");
        
        
        
    }//GEN-LAST:event_btnCrearNotaCreditoActionPerformed

